    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            Empleado empleado = empleadoSeleccionado();
            if (alModificar != null) alModificar.accept(empleado);
            else Mensajes.informacion(this, "Abra este panel desde el menú principal para modificar el empleado.");
        } catch (RuntimeException e) {
            Mensajes.error(this, e.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        try {
            Empleado empleado = empleadoSeleccionado();
            if (Mensajes.confirmarEliminacion(this)) {
                empleadoControlador.eliminar(empleado.getDni());
                cargarTabla();
                Mensajes.informacion(this, "Empleado eliminado correctamente.");
            }
        } catch (RuntimeException e) {
            Mensajes.error(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


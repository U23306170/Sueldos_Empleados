    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            String dni = util.ValidadorEmpleado.dni(txtBuscarDni.getText());
            Empleado empleado = empleadoControlador.buscarPorDni(dni);
            if (empleado == null) { Mensajes.informacion(this, "Empleado no encontrado."); return; }
            DefaultTableModel modelo = (DefaultTableModel) tableRegistro.getModel();
            modelo.setRowCount(0); agregarFila(modelo, empleado);
        } catch (RuntimeException e) {
            Mensajes.error(this, e.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void configurarTabla() {
        tableRegistro.setModel(new DefaultTableModel(new Object[][]{}, new String[]{"DNI", "Nombre", "Área", "Hijos", "Días", "Neto"}) {
            @Override public boolean isCellEditable(int fila, int columna) { return false; }
        });
    }

    private void cargarTabla() {
        try {
            DefaultTableModel modelo = (DefaultTableModel) tableRegistro.getModel(); modelo.setRowCount(0);
            for (Empleado empleado : empleadoControlador.listar()) agregarFila(modelo, empleado);
        } catch (RuntimeException e) { Mensajes.error(this, e.getMessage()); }
    }

    private void agregarFila(DefaultTableModel modelo, Empleado empleado) {
        String neto = NumberFormat.getCurrencyInstance(new Locale("es", "PE")).format(empleado.calcularSueldoNeto());
        modelo.addRow(new Object[]{empleado.getDni(), empleado.getNombre(), empleado.getArea().getNombre(), empleado.getNumeroHijos(), empleado.getDiasAsistidos(), neto});
    }

    private Empleado empleadoSeleccionado() {
        int fila = tableRegistro.getSelectedRow();
        if (fila < 0) throw new IllegalArgumentException("Seleccione un empleado en la tabla.");
        Empleado empleado = empleadoControlador.buscarPorDni(String.valueOf(tableRegistro.getValueAt(fila, 0)));
        if (empleado == null) throw new IllegalArgumentException("El empleado ya no existe.");
        return empleado;
    }

